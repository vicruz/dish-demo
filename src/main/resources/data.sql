DROP TABLE IF EXISTS PROJECT_TECHNOLOGY;
DROP TABLE IF EXISTS PROJECT_DEVELOPER;
DROP TABLE IF EXISTS PROJECT;
DROP TABLE IF EXISTS TECHNOLOGY;
DROP TABLE IF EXISTS DEVELOPER;

CREATE TABLE PROJECT(
	PROJECT_ID INT AUTO_INCREMENT PRIMARY KEY, 
	PROJECT_NAME VARCHAR(255),
	START_DATE DATE,
	PROJECT_DESCRIPTION VARCHAR(255),
	PROJECT_MANAGER VARCHAR(255)
);


CREATE TABLE TECHNOLOGY(
	TECHNOLOGY_ID INT AUTO_INCREMENT PRIMARY KEY, 
	TECHNOLOGY_NAME VARCHAR(255)
);

CREATE TABLE DEVELOPER(
	DEVELOPER_ID INT AUTO_INCREMENT PRIMARY KEY,
	DEVELOPER_NAME VARCHAR(255)
);

CREATE TABLE PROJECT_DEVELOPER(
	PROJECT_ID INT NOT NULL,
	DEVELOPER_ID INT NOT NULL,
	FOREIGN KEY(PROJECT_ID) REFERENCES PROJECT(PROJECT_ID),
	FOREIGN KEY(DEVELOPER_ID) REFERENCES DEVELOPER(DEVELOPER_ID),
	PRIMARY KEY(PROJECT_ID, DEVELOPER_ID) 
);

CREATE TABLE PROJECT_TECHNOLOGY(
	PROJECT_ID INT NOT NULL,
	TECHNOLOGY_ID INT NOT NULL,
	FOREIGN KEY(PROJECT_ID) REFERENCES PROJECT(PROJECT_ID),
	FOREIGN KEY(TECHNOLOGY_ID) REFERENCES TECHNOLOGY(TECHNOLOGY_ID),
	PRIMARY KEY(PROJECT_ID, TECHNOLOGY_ID) 
);

